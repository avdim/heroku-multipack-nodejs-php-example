<div style="text-align: justify">
    Самая большая детская площадка Little Star расположен на 3 этаже ТЦ Авиапарк. До торгового центра ходят бесплатные автобусы с интервалом 10-15 минут от станций метро Динамо, Савеловская.
    <br/>
    Адрес: Москва, Ходынский бульвар, д. 4
    <br/>
    Работаем в режиме торгового центра.
    <br/>
    Площадка рассчитана для детей от 8 месяцев до 12 лет.
    <br/>
    Яркие аттракционы приведут в восторг самых маленьких посетителей! Веселые горки, забавные карусели, водный батут, кораблик, осьминог и многое другое – здесь есть все для веселых и безопасных игр! Также нашим гостям могут понравится 2 батутные арены на разные возраста с поролоновой ямой и небольшим скалодромом.
    <br/>
    Малыши будут в восторге от огромного количества игрушек и развлечений. Чего тут только нет! И железная дорога, и магазин, и мастерская, кухня, машинки, каталки… Все детские мечты станут реальностью. А родители могут посидеть в это время рядом с детьми на удобных диванах.
    <br/>
    В нашем сладком домике Вы можете отпраздновать самый сказочный день рождения Вашего ребенка! Мы также можем Вам предложить дополнительную развлекательную программу по Вашему желанию. Для бронирования сказочного домика звоните нашему Управляющему по телефону + 7 (977)822-11-21.
    <br/>
    Мы делаем все возможное, чтобы наши маленькие гости были в восторге от посещения нашей чудесной площадки!
